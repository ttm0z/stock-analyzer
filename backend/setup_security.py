def generate_secret_key(length=32):
    """Generate a secure secret key"""
    return secrets.token_urlsafe(length)

def get_database_password():
    """Get secure database password"""
    print("\nğŸ” Database Security Setup")
    print("Current database URL uses weak credentials!")
    print("Please provide a strong database password (minimum 16 characters)")
    
    while True:
        password = getpass.getpass("Enter new database password: ")
        if len(password) < 16:
            print("âŒ Password too short. Minimum 16 characters required.")
            continue
        
        confirm = getpass.getpass("Confirm password: ")
        if password != confirm:
            print("âŒ Passwords don't match. Try again.")
            continue
        
        return password

